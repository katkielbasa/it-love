package pl.altkom;

/**
 *
 * @author kursant1
 */
public class Person {
    int z;
    int y;

    public Person(int z, int y) {
        this.z = z;
        this.y = y;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
}
